
package almacenamiento;

public class FuncionSubir {
    public int tipo;
    public Object valor;

    public FuncionSubir() {
        tipo = 0;
        valor = null;
    }

    public FuncionSubir(int tipo_, Object valor_) {
        this.tipo = tipo_;
        this.valor = valor_;
    }
    
    
    
    
}
